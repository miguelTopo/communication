	public static List<DFPlayerItemResponse> getPhrase(String text) {
		if (StringUtils.isEmpty(text))
			return Collections.emptyList();
		//// @formatter:off
		text = text.toLowerCase()
			.replace(",", "")
			.replace("¿", "")
			.replace("?", "")
			.replace(";", "")
			.replace("!", "")
			.replace("¡", "")
			.replace(".", "");
		// @formatter:on

		String[] wordList = text.split(" ");
		if (wordList == null || wordList.length <= 0)
			return Collections.emptyList();
		List<DFPlayerItemResponse> response = new ArrayList<>(wordList.length);
		for (int i = 0; i < wordList.length; i++) {
			for (DFPlayerItem item : DICTIONARY) {
				if (item.getWord().equalsIgnoreCase(wordList[i])) {
					response.add(new DFPlayerItemResponse(item.getFolder(), item.getFile()));
					break;
				}
			}
		}
		return response;
	}

